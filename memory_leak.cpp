#include <bits/stdc++.h> 

#define ARR_SIZE 256

using namespace std; 
  
int main() 
{ 
  
  double *R1= new double[ARR_SIZE];
  double *R2= new double[ARR_SIZE];
  double *R3= new double[ARR_SIZE];
  double *R4= new double[ARR_SIZE];

  for (int i=0; i<ARR_SIZE; ++i)
  {
    R1[i] = 1.0*i;
    R2[i] = 2.0*i;
    R3[i] = 3.0*i;
    R4[i] = 4.0*i;
  }
 
  
  return 0; 
} 

